package trpgsheets.dto;

public class WeaponDto {
	public String weaponName;
	public String hands;
	public String needStr;
	public String hitCorrect;
	public String hit;
	public String power;
	public String critical;
	public String chaseDamage;
	public String memo;

	public String getWeaponName() {
		return weaponName;
	}
	public void setWeaponName(String weaponName) {
		this.weaponName = weaponName;
	}
	public String getHands() {
		return hands;
	}
	public void setHands(String hands) {
		this.hands = hands;
	}
	public String getNeedStr() {
		return needStr;
	}
	public void setNeedStr(String needStr) {
		this.needStr = needStr;
	}
	public String getHitCorrect() {
		return hitCorrect;
	}
	public void setHitCorrect(String hitCorrect) {
		this.hitCorrect = hitCorrect;
	}
	public String getHit() {
		return hit;
	}
	public void setHit(String hit) {
		this.hit = hit;
	}
	public String getPower() {
		return power;
	}
	public void setPower(String power) {
		this.power = power;
	}
	public String getCritical() {
		return critical;
	}
	public void setCritical(String critical) {
		this.critical = critical;
	}
	public String getChaseDamage() {
		return chaseDamage;
	}
	public void setChaseDamage(String chaseDamage) {
		this.chaseDamage = chaseDamage;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
}
